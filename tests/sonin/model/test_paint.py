from sonin.model.hypercube import AbsPosition, Vector
from sonin.model.paint import CityShape, RectangleShape


@mark.parametrize(
    'center, size, expected',
    [
        (Vector.of([0], 10), 1, [(0,)]),
        (Vector.of([0, 0], 10), 1, [(0, 0)]),
        (Vector.of([0, 0, 0], 10), 1, [(0, 0, 0)]),

        (Vector.of([0], 10), 3, [(0,), (1,)]),

        (Vector.of([0, 0], 10), 3, [
            (0, 0),
            (0, 1),
            (1, 0),
            (1, 1),
        ]),

        # [][] [][]
        # CT[] [][]
        (Vector.of([0, 0, 0], 10), 3, [
            (x, y, z)
            for x in range(2)
            for y in range(2)
            for z in range(2)
        ]),

        # ______ __[][] __[]CT
        # ______ __[][] __[][]
        # ______ ______ ______
        (Vector.of([2, 2, 2], 3), 3, [
            (x + 1, y + 1, z + 1)
            for x in range(2)
            for y in range(2)
            for z in range(2)
        ]),

        (Vector.of([0], 10), 5, [(0,), (1,), (2,)]),

        # [][][]
        # [][][]
        # CT[][]
        (Vector.of([0, 0], 10), 5, [
            (x, y)
            for x in range(3)
            for y in range(3)
        ]),

        # [][][] [][][] [][][]
        # [][][] [][][] [][][]
        # CT[][] [][][] [][][]
        (Vector.of([0, 0, 0], 10), 5, [
            (x, y, z)
            for x in range(3)
            for y in range(3)
            for z in range(3)
        ]),

        # []CT[][]
        (Vector.of([1], 10), 5, [(0,), (1,), (2,), (3,)]),

        # [][][][]
        # [][][][]
        # []CT[][]
        (Vector.of([1, 0], 10), 5, [
            (x, y)
            for x in range(4)
            for y in range(3)
        ]),

        # [][][][] [][][][] [][][][] [][][][]
        # [][][][] [][][][] [][][][] [][][][]
        # [][][][] []CT[][] [][][][] [][][][]
        (Vector.of([1, 0, 0], 10), 5, [
            (x, y, z)
            for x in range(4)
            for y in range(3)
            for z in range(3)
        ]),

        # ____________ __[][][][][] __[][][][][] __[][][][][] __[][][][][] __[][][][][]
        # ____________ __[][][][][] __[][][][][] __[][][][][] __[][][][][] __[][][][][]
        # ____________ __[][][][][] __[][][][][] __[][]CT[][] __[][][][][] __[][][][][]
        # ____________ __[][][][][] __[][][][][] __[][][][][] __[][][][][] __[][][][][]
        # ____________ __[][][][][] __[][][][][] __[][][][][] __[][][][][] __[][][][][]
        # ____________ ____________ ____________ ____________ ____________ ____________
        (Vector.of([3, 3, 3], 10), 5, [
            (x + 1, y + 1, z + 1)
            for x in range(5)
            for y in range(5)
            for z in range(5)
        ]),
    ],
)
def test_rectangle_shape(center: Vector, size: int, expected: list[tuple[int, ...]]):
    shape = RectangleShape(center=AbsPosition(value=center), sizes=(size,) * center.num_dimensions)
    actual = [position.value for position in shape.positions(center.num_dimensions, center.dimension_size)]

    assert actual == expected


